'use strict';
/**
 * Функция операции сложения
 * @param {*} a 
 * @param {*} b 
 * @returns сумму
 */
function sumNumb(a, b){
    return a + b
}

/**
 * Функция вычитания
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
function substractingNumb(a, b) {
    return a - b
}

/**
 * Функция деления
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
function divisionNumb(a, b) {
    return a / b
}

/**
 * Функция умножения
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
function multiplicationNumb(a, b){
    return a * b
}